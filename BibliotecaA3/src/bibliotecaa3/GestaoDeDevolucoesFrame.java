    private void carregarEmprestimos() {
        DefaultTableModel model = (DefaultTableModel) tblEmprestimoCadastrado.getModel();
        model.setRowCount(0); // Limpa a tabela
        String sql = "SELECT ID, ID_Livro, ID_Membro, Data_Emprestimo, Data_Devolucao, Status FROM emprestimos";
        try (Connection conn = DriverManager.getConnection(URL, USER, PASS);
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {
            while (rs.next()) {
                model.addRow(new Object[] {
                    rs.getInt("ID"),
                    rs.getInt("ID_Livro"),
                    rs.getInt("ID_Membro"),
                    rs.getDate("Data_Emprestimo"),
                    rs.getDate("Data_Devolucao"),
                    rs.getString("Status")
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                "Erro ao carregar empréstimos:\n" + ex.getMessage(),
                "Erro de Conexão",
                JOptionPane.ERROR_MESSAGE);
        }
    }
    
