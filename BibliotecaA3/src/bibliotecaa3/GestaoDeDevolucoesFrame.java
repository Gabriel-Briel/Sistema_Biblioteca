    private void btnRegistrarDevolucaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarDevolucaoActionPerformed
        // TODO add your handling code here:
        
    // Lê o texto digitado no campo do ID e remove espaços extras
String sIdEmprestimo = txtIdLivro.getText().trim();
// Lê o texto digitado no campo da data de devolução
String dataDevStr = dcDataDevolucao.getText().trim();

// Verifica se algum campo está vazio
if (sIdEmprestimo.isEmpty() || dataDevStr.isEmpty()) {
    JOptionPane.showMessageDialog(this, 
        "Preencha todos os campos!", "Atenção", JOptionPane.WARNING_MESSAGE);
    return; // Interrompe a execução se algum campo estiver vazio
}

// Tenta converter o texto do ID para número inteiro
int id;
try {
    id = Integer.parseInt(sIdEmprestimo);
} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(this, 
        "O ID deve ser um número inteiro.", "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
    return; // Interrompe se não for número válido
}

// Tenta converter o texto da data em um objeto Date usando o formato esperado
Date dataDev;
try {
    dataDev = new java.text.SimpleDateFormat("dd/MM/yyyy").parse(dataDevStr);
} catch (Exception ex) {
    JOptionPane.showMessageDialog(this, 
        "Data inválida! Use o formato dd/MM/yyyy", "Erro de Data", JOptionPane.ERROR_MESSAGE);
    return; // Interrompe se a data for inválida
}

// Monta o comando SQL para atualizar a devolução no banco
String sql = "UPDATE emprestimos SET Data_Devolucao = ?, Status = 'Devolvido' WHERE ID = ?";

// Abre conexão com o banco e executa o comando
try (Connection conn = DriverManager.getConnection(URL, USER, PASS);
     PreparedStatement ps = conn.prepareStatement(sql)) {

    // Preenche os parâmetros da query: 1 = data de devolução, 2 = ID do empréstimo
    ps.setDate(1, new java.sql.Date(dataDev.getTime()));
    ps.setInt(2, id);

    // Executa o update e verifica se alguma linha foi alterada
    int linhasAfetadas = ps.executeUpdate();
    if (linhasAfetadas > 0) {
        JOptionPane.showMessageDialog(this, 
            "Devolução registrada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        // Limpa os campos do formulário
        txtIdLivro.setText("");
        dcDataDevolucao.setText("");
        // Atualiza a tabela exibida
        carregarEmprestimos();
    } else {
        JOptionPane.showMessageDialog(this, 
            "Nenhum empréstimo encontrado com o ID informado.", "Aviso", JOptionPane.WARNING_MESSAGE);
    }
} catch (SQLException ex) {
    JOptionPane.showMessageDialog(this, 
        "Erro ao registrar devolução:\n" + ex.getMessage(), "Erro de Conexão", JOptionPane.ERROR_MESSAGE);
}

        
    }//GEN-LAST:event_btnRegistrarDevolucaoActionPerformed

