    private void btnExcluirLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirLivroActionPerformed
        // TODO add your handling code here:

        // ===== INÃCIO do bloco JDBC para EXCLUSÃO DE LIVRO =====
        String idStr = txtIdLivro.getText().trim();
        if (idStr.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Informe o ID do livro a excluir.",
                "AtenÃ§Ã£o",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        int id;
        try {
            id = Integer.parseInt(idStr);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                "ID invÃ¡lido. Informe um nÃºmero inteiro.",
                "Erro",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "DELETE FROM livros WHERE ID = ?";
        try (Connection conn = DriverManager.getConnection(URL, USER, PASS);
            PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, id);
            int linhas = ps.executeUpdate();
            if (linhas > 0) {
                JOptionPane.showMessageDialog(this,
                    "Livro excluÃ­do com sucesso!",
                    "Sucesso",
                    JOptionPane.INFORMATION_MESSAGE);
                txtIdLivro.setText("");
            } else {
                JOptionPane.showMessageDialog(this,
                    "Nenhum livro encontrado com o ID informado.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                "Erro ao excluir no banco:\n" + ex.getMessage(),
                "Erro de ConexÃ£o",
                JOptionPane.ERROR_MESSAGE);
        }
        // ===== FIM do bloco JDBC =====
    }//GEN-LAST:event_btnExcluirLivroActionPerformed

    private void btnCadastarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastarLivroActionPerformed

        // ===== INÃCIO do bloco JDBC para CADASTRO DE LIVRO com ENUM('Sim','NÃ£o') =====
        // 1) LÃª os valores dos campos
        String titulo      = txtTituloLivro.getText().trim();
        String autor       = txtAutorLivro.getText().trim();
        String categoria   = txtCategoriaLivro.getText().trim();
        String disponivel  = txtDisponibilidadeLivro.getText().trim();

        // 2) ValidaÃ§Ãµes bÃ¡sicas
        if (titulo.isEmpty() || autor.isEmpty() || disponivel.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "TÃ­tulo, Autor e Disponibilidade sÃ£o obrigatÃ³rios!",
                "AtenÃ§Ã£o",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!disponivel.equalsIgnoreCase("Sim") && !disponivel.equalsIgnoreCase("NÃ£o")) {
            JOptionPane.showMessageDialog(this,
                "Disponibilidade deve ser 'Sim' ou 'NÃ£o'!",
                "AtenÃ§Ã£o",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3) Prepara e executa o INSERT
        String sql = "INSERT INTO livros (Titulo, Autor, Categoria, Disponibilidade) VALUES (?, ?, ?, ?)";
        try (Connection conn = DriverManager.getConnection(URL, USER, PASS);
            PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, titulo);
            ps.setString(2, autor);
            ps.setString(3, categoria);
            // grava 'Sim' ou 'NÃ£o' conforme ENUM no banco
            ps.setString(4, disponivel.equalsIgnoreCase("Sim") ? "Sim" : "NÃ£o");
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                "Livro cadastrado com sucesso!",
                "Sucesso",
                JOptionPane.INFORMATION_MESSAGE);

            // limpa os campos
            txtTituloLivro.setText("");
            txtAutorLivro.setText("");
            txtCategoriaLivro.setText("");
            txtDisponibilidadeLivro.setText("");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                "Erro ao salvar no banco:\n" + ex.getMessage(),
                "Erro de ConexÃ£o",
                JOptionPane.ERROR_MESSAGE);
        }
        // ===== FIM do bloco JDBC =====

    }//GEN-LAST:event_btnCadastarLivroActionPerformed

