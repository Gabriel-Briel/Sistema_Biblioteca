        /**
     * Carrega todos os empréstimos no model da JTable.
     */
    private void carregarEmprestimos() {
        var model = (javax.swing.table.DefaultTableModel) tblEmprestimos.getModel();
        model.setRowCount(0);
        String sql = """
            SELECT ID, ID_Livro, ID_Membro,
                   Data_Emprestimo, Data_Devolucao, Status
              FROM emprestimos
            """;
        try (java.sql.Connection conn = java.sql.DriverManager.getConnection(URL,USER,PASS);
             java.sql.Statement  stmt = conn.createStatement();
             java.sql.ResultSet  rs   = stmt.executeQuery(sql)) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("ID"),
                    rs.getInt("ID_Livro"),
                    rs.getInt("ID_Membro"),
                    rs.getDate("Data_Emprestimo"),
                    rs.getDate("Data_Devolucao"),
                    rs.getString("Status")
                });
            }
        } catch (java.sql.SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Erro ao carregar empréstimos:\n" + ex.getMessage(),
                "Erro de Conexão",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    
