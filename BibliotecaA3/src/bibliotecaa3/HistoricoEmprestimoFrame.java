    private void btnRegistrarEmprestimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarEmprestimoActionPerformed
        // TODO add your handling code here:
        
        // 1) Lê e valida
    String sIdL = txtIdLivro.getText().trim();
    String sIdM = txtIdMembro.getText().trim();
    java.util.Date dtE = (java.util.Date) dcDataEmprestimo.getValue();
    java.util.Date dtD = (java.util.Date) dcDataDevolucao.getValue();
    String status = (String) cmbStatus.getSelectedItem();

    if (sIdL.isEmpty() || sIdM.isEmpty() || dtE==null || dtD==null) {
        JOptionPane.showMessageDialog(this,
            "Preencha todos os campos!",
            "Atenção",
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    int idL, idM;
    try {
        idL = Integer.parseInt(sIdL);
        idM = Integer.parseInt(sIdM);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this,
            "IDs devem ser números inteiros.",
            "Erro de Entrada",
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 2) Insere no banco
    String sql = """
        INSERT INTO emprestimos
          (ID_Livro, ID_Membro, Data_Emprestimo, Data_Devolucao, Status)
        VALUES (?, ?, ?, ?, ?)
        """;
    try (java.sql.Connection conn = DriverManager.getConnection(URL,USER,PASS);
         java.sql.PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt  (1, idL);
        ps.setInt  (2, idM);
        ps.setDate (3, new java.sql.Date(dtE.getTime()));
        ps.setDate (4, new java.sql.Date(dtD.getTime()));
        ps.setString(5, status);
        ps.executeUpdate();

        JOptionPane.showMessageDialog(this,
            "Empréstimo registrado com sucesso!",
            "Sucesso",
            JOptionPane.INFORMATION_MESSAGE);

        // 3) Recarrega a tabela e limpa
        carregarEmprestimos();
        txtIdLivro.setText("");
        txtIdMembro.setText("");
        dcDataEmprestimo.setValue(null);
        dcDataDevolucao.setValue(null);
        cmbStatus.setSelectedIndex(0);

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this,
            "Erro ao registrar empréstimo:\n" + ex.getMessage(),
            "Erro de Conexão",
            JOptionPane.ERROR_MESSAGE);
    }

        
    }//GEN-LAST:event_btnRegistrarEmprestimoActionPerformed

