    private void btnAtualizarMembroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarMembroActionPerformed
        // TODO add your handling code here:
        
    // ===== IN√çCIO do bloco JDBC =====
String nome     = txtNomeMembro.getText().trim();
String cpf      = txtCpfMembro.getText().trim();
String telefone = txtTelefoneMembro.getText().trim();
String email    = txtEmailMembro.getText().trim();

if (nome.isEmpty() || cpf.isEmpty()) {
    JOptionPane.showMessageDialog(this,
        "Nome e CPF s√£o obrigat√≥rios!",
        "Aten√ß√£o",
        JOptionPane.WARNING_MESSAGE);
    return;
}

String sql = "INSERT INTO membros (Nome, CPF, Telefone, Email) VALUES (?, ?, ?, ?)";
try (Connection conn = DriverManager.getConnection(URL, USER, PASS);
     PreparedStatement ps = conn.prepareStatement(sql)) {

    ps.setString(1, nome);
    ps.setString(2, cpf);
    ps.setString(3, telefone);
    ps.setString(4, email);
    ps.executeUpdate();

    JOptionPane.showMessageDialog(this,
        "Membro cadastrado com sucesso!",
        "Sucesso",
        JOptionPane.INFORMATION_MESSAGE);

    txtNomeMembro.setText("");
    txtCpfMembro.setText("");
    txtTelefoneMembro.setText("");
    txtEmailMembro.setText("");

} catch (SQLException ex) {
    JOptionPane.showMessageDialog(this,
        "Erro ao salvar no banco:\n" + ex.getMessage(),
        "Erro de Conex√£o",
        JOptionPane.ERROR_MESSAGE);
}
// ===== FIM do bloco JDBC =====

        
    }//GEN-LAST:event_btnAtualizarMembroActionPerformed

    private void btnExcluirMembroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirMembroActionPerformed
        // TODO add your handling code here:
        
        // ===== IN√çCIO do bloco de EXCLUS√ÉO =====
    String idStr = txtIdMembro.getText().trim();
    if (idStr.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Informe o ID do membro a excluir.",
            "Aten√ß√£o",
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    int id;
    try {
        id = Integer.parseInt(idStr);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this,
            "ID inv√°lido. Informe um n√∫mero inteiro.",
            "Erro",
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    String sql = "DELETE FROM membros WHERE ID = ?";
    try (Connection conn = DriverManager.getConnection(URL, USER, PASS);
         PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setInt(1, id);
        int linhas = ps.executeUpdate();
        if (linhas > 0) {
            JOptionPane.showMessageDialog(this,
                "Membro exclu√≠do com sucesso!",
                "Sucesso",
                JOptionPane.INFORMATION_MESSAGE);
            txtIdMembro.setText("");
        } else {
            JOptionPane.showMessageDialog(this,
                "Nenhum membro encontrado com o ID informado.",
                "Aviso",
                JOptionPane.WARNING_MESSAGE);
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this,
            "Erro ao excluir no banco:\n" + ex.getMessage(),
            "Erro de Conex√£o",
            JOptionPane.ERROR_MESSAGE);
    }
    // ===== FIM do bloco de EXCLUS√ÉO =====
    }//GEN-LAST:event_btnExcluirMembroActionPerformed

